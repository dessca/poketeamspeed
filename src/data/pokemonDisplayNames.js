export function getEnglishPokemonName(dexNo, formKey = "base") {
  const baseName = BASE_ENGLISH_NAME_BY_DEX[dexNo] || "";
  if (!baseName) return "";
  if (formKey === "base") return baseName;

  const overrideKey = `${dexNo}:${formKey}`;
  if (FORM_NAME_OVERRIDES[overrideKey]) return FORM_NAME_OVERRIDES[overrideKey];
  if (FORM_PREFIX[formKey]) return `${FORM_PREFIX[formKey]} ${baseName}`;
  return baseName;
}

